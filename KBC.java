class RandomNumber
{
	HashMap<Integer,Integer> hm;
	int i;
	public RandomNumber()
	{
		i=0;
		hm=new HashMap<Integer,Integer>();
	}
	int randomNumber()
	{
		int rand=(int)(Math.random()*20);
		Integer key=new Integer(i);
		if(hm.isEmpty())
		{
			Integer value=new Integer(rand);
			hm.put(key,value);
			i++;
			return rand;
		}
		else
		{
			Integer value;
			value=new Integer(rand);
			while(hm.containsValue(value))
			{
				rand=(int)(Math.random()*20);
				value=new Integer(rand);
			}
			hm.put(key,value);
			i++;
			return rand;
		}
	}
}
